public class LeaderboardModelClass {
    String actor;
    int fame;

    public LeaderboardModelClass(String actor, int fame) {
        this.actor = actor;
        this.fame = fame;
    }

    public String getActor() {
        return actor;
    }

    public int getFame() {
        return fame;
    }






}
